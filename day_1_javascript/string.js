// you can use single quotes double quotes or backticks to generates strings
// in Javascript. Single quotes and double quotes are the same

'This is a string'

"This is also a string"

`Yet this is also a string`

'11'

"123"

`4353`

'I can\'t do that'
"I can't do that"

// String concatenation using `+`
'Tam' + 'Kbeili'

// String interpolation (ES6 feature)
`Hello ${1 + 1}`

// remember that string interpolation only works with backticks and not with
// single or double quotes

// we can use parseInt or parseFloat to convert a string to a number (integer
// or float)
parseInt("123.232")
// 123
parseFloat("123.232")
// 123.232
